<template>
  <header class="header">
    <a class="toogle" @click="toggleMenu" v-if="!hideToogle">
      <i class="fa fa-lg" :class="icon"></i>
    </a>
    <h1 class="title">{{ title }}</h1>
  </header>
</template>

<script>
export default {
  name: "Header",
  props: {
    title: String,
    hideToogle: Boolean
  },
  computed: {
    icon() {
      return this.$store.state.isMenuVisible
        ? "fa-angle-left"
        : "fa-angle-down";
    }
  },
  methods: {
    toggleMenu() {
      this.$store.commit("toggleMenu");
    }
  }
};
</script>

